int superEggDrop(int K, int N) {
	//k is egg
	if (N == 0 || N == 1) {
		return N;
	}
	if (K == 1)
		return N;
	if (K == 0)
		return 0;
	if (t[K][N] != -1) {
		return t[K][N];
	}
	int mn = INT_MAX;
	for (int k = 1; k <= N; k++) {
		int temp = 1 + max(superEggDrop(K - 1, k - 1), superEggDrop(K, N - k));
		mn = min(temp, mn);
	}
	t[K][N] = mn;
	return mn;
}
